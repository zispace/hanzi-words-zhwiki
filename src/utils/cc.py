def convert_zh(converter: OpenCC, word: str) -> str:
    """繁体转换成简体"""
    return ZH_WORDS[word] if word in ZH_WORDS else converter.convert(word)


def filter_words(converter: OpenCC, words: list[str]) -> list[str]:
    """繁体转为简体并过滤重复词语，最后按长度排序"""
    word_set = set(words)
    word_out = set()
    trans = str.maketrans(ZH_CHARS)
    for w in word_set:
        w1 = w.translate(trans)
        w2 = convert_zh(converter, w1)
        if w2.lower() in word_out:
            continue
        word_out.add(w2)

    word_list = sorted(word_out, key=lambda x: (len(x), x.lower()))
    return word_list


def format_word(
    word: str,
    pattern_postfix: str,
    pattern_en,
    pattern_has_cjk: str,
    pattern_cjk: str,
    pattern_cjk_more: str,
    pattern_cjk_extra: str,
    min_word_len: int = 2,
) -> tuple[int, str]:
    word = unicodedata.normalize("NFKC", word.strip())  # 替换兼容字符
    word = re.sub(pattern_postfix, "", word.strip())
    word = re.sub(r"[\s_]+", " ", word).strip()

    flag = 0
    if len(word) < min_word_len:
        return flag, word
    if word.endswith(("/翻译", "/衍生词", "/翻譯", "/衍生詞")) or word.startswith("不支援的"):
        return flag, word

    if re.findall(pattern_has_cjk, word):
        if re.match(pattern_cjk, word):
            flag = 1
        else:
            word = re.sub(r"[・‧•]", "·", word)
            word = re.sub(r"[─–－]", "-", word)
            word = re.sub(r"([^—])—([^—])", r"\1-\2", word)
            word = re.sub(r"[／]", "/", word)
            word = re.sub(r"[．]", ".", word)

            if re.match(pattern_cjk_more, word):
                word = word.strip("·-/")
                flag = 2
            elif re.match(pattern_cjk_extra, word, flags=re.ASCII):
                flag = 3

    else:
        # 纯英文
        if re.match(pattern_en, word) and re.findall(r"\w", word, flags=re.ASCII):
            flag = 4
    return flag, word
